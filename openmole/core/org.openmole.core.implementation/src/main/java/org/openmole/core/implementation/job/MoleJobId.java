package org.openmole.core.implementation.job;

import org.openmole.core.model.job.IMoleJobId;

public class MoleJobId implements IMoleJobId {

    final Long id;

    public MoleJobId(Long id) {
        super();
        this.id = id;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        MoleJobId other = (MoleJobId) obj;
        if (id == null) {
            if (other.id != null) {
                return false;
            }
        } else if (!id.equals(other.id)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return Long.toString(id);
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public int compareTo(IMoleJobId o) {
        return getId().compareTo(o.getId());
    }
}
